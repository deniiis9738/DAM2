@Composable
fun counter() {
    var count by rememberSaveable { mutableStateOf(0) }

    Column(
        modifier = Modifier.fillMaxSize(),
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.Center
    ) {
        Button(
            onClick = { count++ }
        ) {
            Text(text = "PÃºlsame")
        }
        Text(text = "Se ha pulsado $count veces")
    }
}